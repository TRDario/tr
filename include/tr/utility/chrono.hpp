#pragma once
#include "common.hpp"

namespace tr {
	/** @ingroup utility
	 *  @defgroup chrono Chrono
	 *  Common chrono typedefs.
	 *  @{
	 */

	/******************************************************************************************************************
	 * Shorthand for the standard steady clock.
	 ******************************************************************************************************************/
	using clock = std::chrono::steady_clock;

	/******************************************************************************************************************
	 * Shorthand for the standard steady clock time point.
	 ******************************************************************************************************************/
	using time_point = clock::time_point;

	/******************************************************************************************************************
	 * Shorthand for the standard steady clock duration.
	 ******************************************************************************************************************/
	using duration = clock::duration;

	/******************************************************************************************************************
	 * Shorthand template for second types.
	 ******************************************************************************************************************/
	template <class T> using secs = std::chrono::duration<T>;

	/******************************************************************************************************************
	 * Shorthand for a float second.
	 ******************************************************************************************************************/
	using fsecs = secs<float>;

	/******************************************************************************************************************
	 * Shorthand for a double second.
	 ******************************************************************************************************************/
	using dsecs = secs<double>;

	/******************************************************************************************************************
	 * Shorthand for an integer second.
	 ******************************************************************************************************************/
	using isecs = std::chrono::seconds;

	/******************************************************************************************************************
	 * Shorthand template for millisecond types.
	 ******************************************************************************************************************/
	template <class T> using msecs = std::chrono::duration<T, std::milli>;

	/******************************************************************************************************************
	 * Shorthand for a float millisecond.
	 ******************************************************************************************************************/
	using fmsecs = msecs<float>;

	/******************************************************************************************************************
	 * Shorthand for a double millisecond.
	 ******************************************************************************************************************/
	using dmsecs = msecs<double>;

	/******************************************************************************************************************
	 * Shorthand for an integer millisecond.
	 ******************************************************************************************************************/
	using imsecs = std::chrono::milliseconds;

	/******************************************************************************************************************
	 * Shorthand template for microsecond types.
	 ******************************************************************************************************************/
	template <class T> using usecs = std::chrono::duration<T, std::micro>;

	/******************************************************************************************************************
	 * Shorthand for a float microsecond.
	 ******************************************************************************************************************/
	using fusecs = usecs<float>;

	/******************************************************************************************************************
	 * Shorthand for a double microsecond.
	 ******************************************************************************************************************/
	using dusecs = usecs<double>;

	/******************************************************************************************************************
	 * Shorthand for an integer microsecond.
	 ******************************************************************************************************************/
	using iusecs = std::chrono::microseconds;

	/******************************************************************************************************************
	 * Shorthand template for nanosecond types.
	 ******************************************************************************************************************/
	template <class T> using nsecs = std::chrono::duration<T, std::nano>;

	/******************************************************************************************************************
	 * Shorthand for a float nanosecond.
	 ******************************************************************************************************************/
	using fnsecs = nsecs<float>;

	/******************************************************************************************************************
	 * Shorthand for a double nanosecond.
	 ******************************************************************************************************************/
	using dnsecs = nsecs<double>;

	/******************************************************************************************************************
	 * Shorthand for an integer nanosecond.
	 ******************************************************************************************************************/
	using insecs = std::chrono::nanoseconds;

	/// @}
} // namespace tr
