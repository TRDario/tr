////////////////////////////////////////////////////////////// TO UNDERLYING //////////////////////////////////////////////////////////////

template <tr::enumerator Enum> constexpr std::underlying_type_t<Enum> tr::to_underlying(Enum enumerator)
{
	return static_cast<std::underlying_type_t<Enum>>(enumerator);
}