#include <gtest/gtest.h>
#include <tr/utility/defer.hpp>

TEST(defer_test, increment)
{
	int i = 0;
	{
		TR_DEFER(++i);
		EXPECT_EQ(i, 0);
	}
	EXPECT_EQ(i, 1);
}